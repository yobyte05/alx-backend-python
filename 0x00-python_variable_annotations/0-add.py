#!/usr/bin/env python3

"""Write a type-annotated function add that takes a float a
and a float b as arguments and returns their sum as a float."""

def add(a: float, b: float) -> float:
    """
    Return the sum of two floating-point numbers.
    Arguments:
    a -- The first floating-point number
    b -- The second floating-point number

    Returns:
    The sum of the two input numbers as a floating-point value.
    """
    return a + b
